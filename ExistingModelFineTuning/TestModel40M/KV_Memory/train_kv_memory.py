#!/usr/bin/env python3
"""Train a trainable, routed KV *memory* on top of the pretrained 40M HA model.

Proof of concept
----------------
The pretrained dense checkpoint (``speed_run_dense_muon_final.pt``) is loaded into
:class:`HAMemoryLM`.  A fixed pool of KV-cache chunks ("topic memory", 128 topics
x ``--chunks-per-topic`` chunks x 64 tokens, default ~16K) is initialised from the
skill text generated by ``generate_skill_kv_chunks_v1.py`` and kept resident in VRAM.
Each topic's tokens are *copied* into its chunks: the copies start identical but sit
at different positions and accumulate their own gradients, so every topic gets more
trainable memory capacity.

During training every text sequence is placed *after* the memory (positions are
shifted by the memory length).  Hierarchical routing selects, per query chunk,
only the few memory chunks relevant to the current topic, so the loss gradient
reaches just those chunks — a sparse, MoE-like update of the memory.  The memory
tensors are leaf parameters with no history back to the embeddings: gradients
change the KV memory but propagate no further.

The model weights are trainable only for the first ``--weight-train-tokens``
tokens (default 50M, lr ``--weight-lr`` = 5e-5).  Beyond that budget the weights
are frozen and *only* the KV memory keeps training: tests show that, given more
tokens, the weights start exploiting the routing to peek at the next token
(breaking causality), so the early budget adapts the weights to the memory while
staying causal, after which the memory alone continues to learn.

Validation compares, on the same held-out tokens, the loss of:

  * ORIGINAL: the dense checkpoint (memory disabled), and
  * MEMORY:   the same weights with the routed KV memory attached.

Usage
-----
    python -m ExistingModelFineTuning.TestModel40M.KV_Memory.train_kv_memory \
        --steps 2000 --val-tokens 500000

    # quick smoke test
    python ExistingModelFineTuning/TestModel40M/KV_Memory/train_kv_memory.py \
        --smoke
"""

